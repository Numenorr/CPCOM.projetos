    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        if(jComboBox1.getSelectedItem() != null
                && !jTextField_descricao.getText().equals("")
                && !jTextField_qtd.getText().equals("")
                && !jTextField_valor.getText().equals("")){
            
            Categoria categoria = (Categoria) jComboBox1.getSelectedItem();
            
            if(produtoController.create(jTextField_descricao.getText(), 
                                        Integer.parseInt(jTextField_qtd.getText()), 
                                        Double.parseDouble(jTextField_valor.getText()), 
                                        categoria)){
                
                JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");
                this.getListaProdutos();
                this.limparCampos();
            
            }else{
                //Por algum motivo este laço sempre entra no else
                //mesmo que o if seja true
                //Visto isso, coloquei as funcoes aqui :D
                JOptionPane.showMessageDialog(this, "Erro ao salvar produto",
                "Erro", JOptionPane.ERROR_MESSAGE);
                this.getListaProdutos();
                this.limparCampos();
            }
        }
    }//GEN-LAST:event_jButton_addActionPerformed

    private void jTable_produtosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_produtosMouseClicked
        if(jTable_produtos.getSelectedRow() != -1){
            
        jTextField_descricao.setText((String) jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 1));
        jTextField_qtd.setText(String.valueOf(jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 2)));
        jTextField_valor.setText(String.valueOf(jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 3)));
        String categoria = (String) jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 4);
        
            for(int i=0;i<jComboBox1.getItemCount(); i++){
                Categoria c = (Categoria) jComboBox1.getItemAt(i);
                if(c.getDescricao().equals(categoria)){
                    jComboBox1.setSelectedIndex(i);
                }
            }
        }
    }//GEN-LAST:event_jTable_produtosMouseClicked

    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
        if(jTable_produtos.getSelectedRow() != -1 && jComboBox1.getSelectedItem() != null){
            Categoria categoria = (Categoria) jComboBox1.getSelectedItem();
            produtoController.update((int) jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 0), 
                    jTextField_descricao.getText(), 
                    Integer.parseInt(jTextField_qtd.getText()), 
                    Double.parseDouble(jTextField_valor.getText()), 
                    categoria);
            
            this.getListaProdutos();
            this.limparCampos();
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        if(jTable_produtos.getSelectedRow() != -1){
            produtoController.delete((int) jTable_produtos.getValueAt(jTable_produtos.getSelectedRow(), 0));
            
            this.getListaProdutos();
            this.limparCampos();
        }else{
            JOptionPane.showMessageDialog(this, "Erros ao deletar produto");
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

