    public void getListaCategorias(){
            DefaultTableModel modeloCategorias = (DefaultTableModel) jTable1.getModel();
            modeloCategorias.setNumRows(0);
            
            for(Categoria c : categoriaController.read()){
                
                modeloCategorias.addRow(new Object[]{
                    c.getId(),
                    c.getDescricao()});
            }
        }

