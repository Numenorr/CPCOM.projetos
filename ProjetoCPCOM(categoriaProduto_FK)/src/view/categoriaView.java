    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
        if(jTable1.getSelectedRow() != -1){
            categoriaController.update((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0),
                                        jTextField_descricao.getText());
            
            this.getListaCategorias();
            
            jTextField_descricao.setText("");
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(jTable1.getSelectedRow() != -1){
            jTextField_descricao.setText((String) jTable1.getValueAt(
                    jTable1.getSelectedRow(),1));
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        if(jTable1.getSelectedRow() != -1){
            categoriaController.delete((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            
            this.getListaCategorias();
            
            jTextField_descricao.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Erro","Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

    private void jButton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscarActionPerformed
        DefaultTableModel modeloCategorias = (DefaultTableModel) jTable1.getModel();
        modeloCategorias.setNumRows(0);
        
        for (Categoria c : categoriaController.getListCategorias_porDescricao(jTextField_busca.getText())){
            
            modeloCategorias.addRow(new Object[]{
                    c.getId(),
                    c.getDescricao()});
        }
    }//GEN-LAST:event_jButton_buscarActionPerformed

